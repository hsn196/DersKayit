    private void tabloDoldur() {
        List<FacultyMember> fm = new FacultyMemberService().getAll();
        String[] columnNames = new String[7];
        columnNames[0] = "Id";
        columnNames[1] = "Kod";
        columnNames[2] = "Adƒ±";
        columnNames[3] = "Soyadƒ±";
        columnNames[4] = "Email";
        columnNames[5] = "Ba≈ü. Tar.";
        columnNames[6] = "Olu≈ü. Tar.";
        String[][] data = new String[fm.size()][7];
        for (int i = 0; i < fm.size(); i++) {
            FacultyMember ft = fm.get(i);
            data[i][0] = ft.getId().toString();
            data[i][1] = ft.getCode();
            data[i][2] = ft.getName();
            data[i][3] = ft.getSurname();
            data[i][4] = ft.getEmail();
            data[i][5] = "";
            data[i][6] = "";
            if (null != (ft.getStartDate())) {
                data[i][5] = ft.getStartDate().toLocaleString();
            }
            if (null != (ft.getCreated_at())) {
                data[i][6] = ft.getCreated_at().toLocaleString();
            }
        }
        tblHoca.setModel(new DefaultTableModel(data, columnNames));
    }

