    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Student s = new Student();
        s.setCode(txtCode.getText());
        s.setName(txtAd.getText());
        s.setSurname(txtSoyad.getText());
        if (txtId.getText().equals("")) {
            s.setCreated_at(new Date());
            new StudentService().save(s);
            JOptionPane.showMessageDialog(this, "Öğrenci Başarıyla Eklendi.");
        } else {
            Student s2 = new StudentService().getById(Long.valueOf(txtId.getText()));
            s.setCreated_at(s2.getCreated_at());
            s.setId(Long.valueOf(txtId.getText()));
            s.setUpdated_at(new Date());
            new StudentService().update(s);
            JOptionPane.showMessageDialog(this, "Öğrenci Başarıyla Güncellendi.");
        }
        formTemizle();
        tabloDoldur();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void tblStudentMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblStudentMouseClicked
        if (tblStudent.getSelectedRow() != -1) {
            Long Id = Long.valueOf(tblStudent.getModel().getValueAt(tblStudent.getSelectedRow(), 0).toString());
            String code = tblStudent.getModel().getValueAt(tblStudent.getSelectedRow(), 1).toString();
            String name = tblStudent.getModel().getValueAt(tblStudent.getSelectedRow(), 2).toString();
            String surname = tblStudent.getModel().getValueAt(tblStudent.getSelectedRow(), 3).toString();

            txtAd.setText(name);
            txtSoyad.setText(surname);
            txtCode.setText(code);
            txtId.setText(String.valueOf(Id));
        }

    }//GEN-LAST:event_tblStudentMouseClicked

