    private void tabloDoldur() {
        List<Student> s = new StudentService().getAll();
        String[] columnNames = new String[5];
        columnNames[0] = "Id";
        columnNames[1] = "Kod";
        columnNames[2] = "Adı";
        columnNames[3] = "Soyadı";
        columnNames[4] = "Oluş. Tar.";
        String[][] data = new String[s.size()][5];
        for (int i = 0; i < s.size(); i++) {
            Student st = s.get(i);
            data[i][0] = st.getId().toString();
            data[i][1] = st.getCode();
            data[i][2] = st.getName();
            data[i][3] = st.getSurname();
            data[i][4] = st.getCreated_at().toLocaleString();
        }
        tblStudent.setModel(new DefaultTableModel(data, columnNames));
    }

    private void formTemizle() {
        txtAd.setText("");
        txtSoyad.setText("");
        txtCode.setText("");
        txtId.setText("");
    }
